ylabel(hax(2), 'intensity after pre-processing, not scaled');
ylabel(hax(1), 'range');
saveas(gcf, [filename(1:end-4), '.fig']);
print(gcf, [filename(1:end-4), '.png'], '-dpng','-r300','-painters');
% plot saturation mask
